#include "Version.h"

#include <QString>
#include <QStringList>

Version::Version( const QString & ver )
	: QVector< quint16 >( 4, 0 )
{
	QStringList list = ver.split('.');

	for ( int i = 0; i < list.count() && i < 4; ++i )
		( *this )[ i ] = list.at( i ).toUInt();
}

QString
Version::toString( int length ) const
{
	QString a;

	for ( int i = 0; i < length && i < 4 ; ++i )
		a += QString( i ? "." : "" ) += QString::number( at( i ) );

	return a;
}

Version::operator QString() const
{
	return toString( 4 );
}

quint16
Version::major() const
{
	return at( 0 );
}

quint16
Version::minor() const
{
	return at( 1 );
}

quint16
Version::build() const
{
	return at( 2 );
}

quint16
Version::revision() const
{
	return at( 3 );
}

bool
Version::accept( const Version & other ) const
{
	return major() == other.major() &&
		minor() <= other.minor();
}

